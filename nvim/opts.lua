local opt = vim.opt

-- Context
opt.number = true
opt.relativenumber = true
opt.scrolloff = 4
opt.signcolumn = 'yes'

-- Encodings
opt.encoding = 'utf8'
opt.fileencoding = 'utf8'

-- Themes
opt.syntax = 'ON'
opt.termguicolors = true

-- Search
opt.ignorecase = true
opt.smartcase = true
opt.incsearch = true
opt.hlsearch = false

-- Spaces
opt.expandtab = true
opt.shiftwidth = 2
opt.softtabstop = 2
opt.tabstop = 2

-- Splits
opt.splitright = true
opt.splitbelow = true

-- Virtual lines
opt.breakindent = true
